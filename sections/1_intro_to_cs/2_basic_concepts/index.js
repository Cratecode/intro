// This is a comment.

// Say hi.
console.log("hello");
console.log("world");

// Let's make it a method.
function sayHi() {
    console.log("hello");
    console.log("world");
}

// And now, we'll run it five times.
sayHi();
sayHi();
sayHi();
sayHi();
sayHi();

// Constants
const importantNumber = 10;

console.log(importantNumber);
console.log(importantNumber);
console.log(importantNumber);
console.log(importantNumber);
console.log(importantNumber);

// If we want to output something else, instead of changing
// each of these console.logs, we can just edit the value
// of importantNumber.

// Variables. We use "let" to declare variables in Javascript.
let variable = 1;
console.log("my value is: " + variable);

variable = 10;
console.log("my value is: " + variable);
